#include "main.h"
#include <stdio.h>

/**
 * rev_string - Write a function that reverses a string
 * @s: string that will be reversed
 *
 * Return: void
 */

void rev_string(char *s)
{
	int i, x;

	x = 0;

	for (i = 0; i < 0; i++)
	{
		printf("%s\n", s);
	}
	for (i = x - 1; i >= 0; i--)
	{
		printf("%s\n", s);
	}
}
